/**
 *  This function takes three hobbies as arguments and returns them in an array.
 * @param {string} hobby_one 
 * @param {string} hobby_two 
 * @param {string} hobby_three 
 * @returns {Array} hobbies
 */
function hobbies(hobby_one, hobby_two, hobby_three){
    const hobbies = [hobby_one, hobby_two, hobby_three];
    return hobbies;
}

//Export hobbies function
export default hobbies;